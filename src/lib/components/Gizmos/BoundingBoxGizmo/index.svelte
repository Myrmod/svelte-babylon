<script lang="ts">
  import { getRoot } from '$lib/utils/context'

  import * as BABYLON from 'babylonjs'
  import { onDestroy, onMount } from 'svelte'

  const root = getRoot()

  export let name: string = 'BoundingBoxGizmo'
  export let color: BABYLON.Color3 = undefined
  export let gizmoLayer: BABYLON.UtilityLayerRenderer = undefined

  export const gizmo = new BABYLON.BoundingBoxGizmo(color, gizmoLayer)

  onMount(() => {
    try {
      if (root.gizmos[name]) return

      root.gizmos[name] = gizmo

      root.scene.render()
    } catch (error) {
      console.error(error)
    }
  })

  onDestroy(() => {
    delete root.gizmos[name]
    gizmo.dispose()
  })

  $: if (root.gizmos[name]) {
    root.scene.render()
  }
</script>
