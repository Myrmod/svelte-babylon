<h1>Canvas</h1>

<p>
  That's the root of your scene, where all the other elements are placed into. It is basically an
  implementation of the <a href="https://doc.babylonjs.com/divingDeeper/scene">babylon.js scene</a>

  By default a canvas is not directly loaded. If you want it to be loaded immediatly you have to set
  the attribute <strong>lazyload</strong> to false.
</p>
